    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        hapustabel();
        String key = txt_search.getText();
        try {
            Connection conn = (Connection) koneksi.koneksiDB();
            String sql = "Select tb_siswa.*, tb_kelas.id_kelas,komp_keahlian , tb_spp.id_spp,nominal from tb_siswa join tb_kelas "
                    + "on tb_kelas.id_kelas = tb_siswa.id_kelas join tb_spp on tb_spp.id_spp = tb_siswa.id_spp WHERE nama "
                    + "LIKE '%" + key + "%' OR nisn LIKE '%" + key + "%' OR komp_keahlian LIKE '%"+ key +"%' OR alamat LIKE '%"+ key +"%'";
            Statement stat = conn.createStatement();
            ResultSet set = stat.executeQuery(sql);
            while (set.next()) {
                String[] data = {set.getString("nisn"),
                    set.getString("nis"),
                    set.getString("nama"),
                    set.getString("id_kelas"),
                    set.getString("komp_keahlian"),
                    set.getString("alamat"),
                    set.getString("no_telp"),
                    set.getString("id_spp"),
                    set.getString("nominal")};
                tabmode.addRow(data);
            }
            
        } catch (Exception e) {
            Notification panel = new Notification(new Form_Menu_Admin(null), Notification.Type.WARNING, Notification.Location.CENTER_RIGHT,
                    "Ada Kesalahan Di Search Key Release"+ e);
            panel.showNotification();
        }
    }//GEN-LAST:event_txt_searchKeyReleased

