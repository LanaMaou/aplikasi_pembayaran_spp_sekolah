            String table_klik = (Table.getModel().getValueAt(row, 0).toString());
            java.sql.Connection conn = (java.sql.Connection) koneksi.koneksiDB();
            Statement stm = conn.createStatement();
            ResultSet sql = stm.executeQuery("Select tb_siswa.*, tb_kelas.id_kelas,komp_keahlian , tb_spp.id_spp,nominal from"
                    + " tb_siswa join tb_kelas on tb_kelas.id_kelas = tb_siswa.id_kelas join tb_spp on tb_spp.id_spp = tb_siswa.id_spp where nisn='" + table_klik + "'");
